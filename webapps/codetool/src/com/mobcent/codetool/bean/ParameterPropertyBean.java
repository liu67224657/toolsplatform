package com.mobcent.codetool.bean;

public class ParameterPropertyBean {
    private String className;
    private String instanceName;
    private String containClass;
    public String getClassName() {
        return className;
    }
    public void setClassName(String className) {
        this.className = className;
    }
    public String getContainClass() {
        return containClass;
    }
    public void setContainClass(String containClass) {
        this.containClass = containClass;
    }
    public String getInstanceName() {
        return instanceName;
    }
    public void setInstanceName(String instanceName) {
        this.instanceName = instanceName;
    }
}
